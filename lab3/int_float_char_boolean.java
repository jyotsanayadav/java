public class int_float_char_boolean {
    public static void main(String[] args) {

        int a = 10;
        float b = 5.5f;
        char c = 'A';
        boolean d = true;

        System.out.printf("Integer: %d\n", a);
        System.out.printf("Float: %.2f\n", b);
        System.out.printf("Character: %c\n", c);
        System.out.printf("Boolean: %b\n", d);
    }
}
